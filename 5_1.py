# Schrijf (en test) de functie som() die 3 parameters heeft: getal1, getal2 en getal3.
# De return-waarde van de functie moet de som (optelling) van deze parameters zijn!

def som(getal1, getal2, getal3):
    return getal1 + getal2 + getal3


print(som(1, 2, 3))

# maar, stiekem kan ook:

print(sum([1, 2, 3]))
